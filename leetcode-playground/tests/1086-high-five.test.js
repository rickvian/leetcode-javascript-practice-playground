import { highFive } from '../1086-high-five.js';

describe('1086-high-five', () => {
  it("highFive([[1, 91], [1, 92], [2, 93], [2, 99], [2, 98], [2, 97], [1, 60], [1, 58], [2, 100], [1, 61]])", () => {
    const result = highFive([[1, 91], [1, 92], [2, 93], [2, 99], [2, 98], [2, 97], [1, 60], [1, 58], [2, 100], [1, 61]]);
    expect(result).toEqual([[1, 72], [2, 97]]);
  });

  it("highFive([[1, 100], [7, 100], [1, 100], [7, 100], [1, 100], [7, 100], [1, 100], [7, 100], [1, 68], [7, 97]])", () => {
    const result = highFive([[1, 100], [7, 100], [1, 100], [7, 100], [1, 100], [7, 100], [1, 100], [7, 100], [1, 68], [7, 97]]);
    expect(result).toEqual([[1, 93], [7, 99]]);
  });

  it("highFive([[1, 100], [1, 90], [1, 80], [1, 70], [1, 60]])", () => {
    const result = highFive([[1, 100], [1, 90], [1, 80], [1, 70], [1, 60]]);
    expect(result).toEqual([[1, 80]]);
  });

  it("highFive([[1, 50], [2, 80], [1, 70], [2, 60], [1, 90], [2, 40], [1, 100], [2, 30]])", () => {
    const result = highFive([[1, 50], [2, 80], [1, 70], [2, 60], [1, 90], [2, 40], [1, 100], [2, 30]]);
    expect(result).toEqual([[1, 62], [2, 42]]);
  });

  it("highFive([[1, 80], [1, 80], [1, 80], [1, 80], [1, 80]])", () => {
    const result = highFive([[1, 80], [1, 80], [1, 80], [1, 80], [1, 80]]);
    expect(result).toEqual([[1, 80]]);
  });

  it("highFive([[1, 100], [2, 99], [1, 98], [2, 97], [1, 96], [2, 95], [1, 94], [2, 93]])", () => {
    const result = highFive([[1, 100], [2, 99], [1, 98], [2, 97], [1, 96], [2, 95], [1, 94], [2, 93]]);
    expect(result).toEqual([[1, 77], [2, 76]]);
  });
});
